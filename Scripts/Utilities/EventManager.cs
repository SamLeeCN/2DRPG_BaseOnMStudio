[DefaultExecutionOrder(-1000)]
public class EventManager : MonoBehaviour
{
    public static EventManager instance;
    public VoidEventSO backToMenuEvent;
    private void Awake()
    {
        if (instance == null)
        {
            instance = this;
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
